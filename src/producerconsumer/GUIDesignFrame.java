     private void setColor(JPanel pane)
    {
        pane.setBackground(new Color(41,57,80));
    }
    
    private void resetColor(JPanel [] pane, JPanel [] indicators)
    {
        for(int i=0;i<pane.length;i++){
           pane[i].setBackground(new Color(23,35,51));
           
        } for(int i=0;i<indicators.length;i++){
           indicators[i].setOpaque(false);           
        }
        
    }
    
    private void inputError(JSpinner input) {
        Border line = BorderFactory.createLineBorder(new Color(255,0,0),2);
        input.setBorder(line);
    }
    
    private void inputOK(JSpinner input){
        Border line = BorderFactory.createLineBorder(new Color(123,156,225),2);
        input.setBorder(line);
    }
    
    private void inputNoLine(JSpinner input) {
        Border line = BorderFactory.createEmptyBorder();
        input.setBorder(line);
    }
    
    private void inputLock(JSpinner input) {
        input.setEnabled(false);
        inputNoLine(input);
    }
    
    private void inputLock(JSpinner[] inputs){
        for(int i = 0; i < inputs.length; i++) {
            inputLock(inputs[i]);
        }
    }
    
    private void inputDefault(JSpinner input) {
        inputNoLine(input);
        input.setEnabled(true);
    }
    
    private void inputDefault(JSpinner[] inputs){
        for(int i = 0; i < inputs.length; i++) {
            inputDefault(inputs[i]);
        }
    }
    
